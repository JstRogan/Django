# 3. ЗАМЫКАНИЯ (CLOSURES)
# =============================================================================

"""
Замыкание — это когда внутренняя функция "тащит" за собой переменные из внешней, 
даже если внешняя уже всё. Она как будто делает "снимок" окружения.

Это полезно для:
1. Скрытия данных (инкапсуляция без классов)
2. Создания специализированных функций на лету
"""

print("\n=== Замыкания ===")

def setup_access(level):
    # Эта переменная "замыкается" внутри функции check
    def check(name):
        print(f"Проверка доступа для {name}... Уровень: {level}")
        if level > 5:
            return "Доступ разрешен"
        return "Недостаточно прав"
    return check

admin_access = setup_access(10)
guest_access = setup_access(3)

print(admin_access("Олег"))
print(guest_access("Антон"))


