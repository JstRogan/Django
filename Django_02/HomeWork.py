"""
Тут мы смешаем всё сразу:
Декоратор + Замыкание + Функцию как объект + Генератор.
Сделаем систему 'Умных повторов' для нестабильных функций.
"""

print("\n=== CHERRY ON TOP: Smart Retry System ===")

import random

def retry(times):
    def decorator(func):
        def wrapper(*args, **kwargs):
            last_error = None
            for i in range(times):
                try:
                    return func(*args, **kwargs)
                except Exception as e:
                    last_error = e
                    print(f"Попытка {i+1} провалена... Пробуем еще раз.")
            print("Все попытки исчерпаны!")
            raise last_error
        return wrapper
    return decorator

@retry(times=3)
def unstable_api_call():
    if random.random() < 0.7:
        raise ConnectionError("Сервер упал!")
    return "Данные получены успешно!"

try:
    print(unstable_api_call())
except:
    print("Миссия провалена.")


# И вишенка в коде: Генератор для чтения логов (ленивый)
def log_reader(count):
    for i in range(count):
        yield f"LOG_EVENT_{i}: OK"

logs = log_reader(3)
print("Чтение логов:")
print(list(logs))

