"""
Чистая функция — это база функционального программирования. 
Она как математическая формула: y = f(x). 
Если в формулу подставить 5, результат всегда будет одинаковым, 
независимо от погоды за окном или того, сколько раз мы её считали.

Ключевые признаки:
1. Детерминированность (один вход -> один выход)
2. Отсутствие сайд-эффектов (не лезет в чужие переменные, не пишет в файлы, не меняет глобалки)

Зачем это нужно? 
Такой код почти невозможно сломать при рефакторинге, его легко тестировать 
и можно без страха запускать в разных потоках.
"""

# ПЛОХО: Функция зависит от внешнего мира и меняет его
db_connection = True 

def save_data(data):
    if db_connection:
        print(f"Записываю {data} в базу...") # Сайд-эффект: вывод в консоль
    return True

# ХОРОШО: Функция работает только с тем, что ей дали
def format_user_name(first, last):
    return f"{first.capitalize()} {last.capitalize()}"

print("=== Чистые функции ===")
user = format_user_name("иван", "иванов")
print(f"Результат чистой функции: {user}")


