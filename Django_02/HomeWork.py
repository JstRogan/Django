# 2. ГЕНЕРАТОРЫ (GENERATORS)
# =============================================================================

"""
Представь, что тебе нужно прочитать книгу из 1000 страниц. 
Список (list) — это если бы ты попытался запомнить все 1000 страниц разом. Мозг (память) может взорваться.
Генератор — это когда ты читаешь по одной странице, а прочитанную забываешь. 

Мы используем 'yield'. Это как 'return', но функция не умирает, 
а встает на паузу до следующего вызова 'next()'.
"""

print("\n=== Генераторы ===")

# Генератор бесконечного потока ID для заказов
def order_id_generator():
    number = 1
    while True:
        yield f"ORD-{number:04d}"
        number += 1

gen = order_id_generator()
print(next(gen)) # ORD-0001
print(next(gen)) # ORD-0002

# Экономия памяти наглядно
import sys
massive_data_list = [x for x in range(1000000)] # Сразу сожрал кучу памяти
massive_data_gen = (x for x in range(1000000))  # Память почти не тронута

print(f"Размер списка: {sys.getsizeof(massive_data_list) / 1024 / 1024:.2f} MB")
print(f"Размер генератора: {sys.getsizeof(massive_data_gen)} bytes")


