# 4. ДЕКОРАТОРЫ (DECORATORS)
# =============================================================================

"""
Декоратор — это "обертка". Мы не трогаем код функции, но добавляем ей магию.
Работает на замыканиях. 

Пример: ты написал функцию, которая качает картинку. 
Потом понял, что надо замерять время скачивания. 
Вместо того чтобы лезть в 100 функций, ты просто вешаешь @timer.
"""

print("\n=== Декораторы ===")

import time

def benchmark(func):
    def wrapper(*args, **kwargs):
        start = time.perf_counter()
        res = func(*args, **kwargs)
        end = time.perf_counter()
        print(f"Функция {func.__name__} отработала за {end - start:.6f} сек.")
        return res
    return wrapper

@benchmark
def heavy_task():
    # Имитация работы
    return sum(x*x for x in range(1000000))

heavy_task()


